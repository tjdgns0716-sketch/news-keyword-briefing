function addKeywordStat(map, key, score, count = 1) {
  const current = map.get(key) ?? { count: 0, score: 0 };
  const safeCount = Math.max(1, Number(count) || 1);
  current.count += safeCount;
  current.score += score * Math.max(1, Math.log2(safeCount + 1));
  map.set(key, current);
}

function getArticleTermOccurrenceCount(article, term, text) {
  const cachedCount = Number(article.analyzedTermCounts?.[term]);
  if (cachedCount > 0) return cachedCount;
  return Math.max(1, countTermOccurrences(text, term));
}

function buildTermOccurrenceMap(terms, text) {
  return Object.fromEntries(
    [...new Set(terms)]
      .map((term) => [term, countTermOccurrences(text, term)])
      .filter(([, count]) => count > 0),
  );
}

function countTermOccurrences(text, term) {
  const normalized = normalizeText(text);
  const compact = compactForMatching(normalized);
  let count = 0;

  for (const variant of getTermVariants(term)) {
    const variantCompact = compactForMatching(variant);
    if (!variantCompact) continue;

    if (/^[A-Za-z0-9+.\-]+$/.test(variant)) {
      const pattern = new RegExp(`(^|[^A-Za-z0-9])${escapeRegExp(variant)}(?=$|[^A-Za-z0-9])`, "gi");
      count += [...normalized.matchAll(pattern)].length;
      continue;
    }

    count += countCompactOccurrences(compact, variantCompact);
  }

  return count;
}

function getTermVariants(term) {
  const variants = new Set([term]);

  for (const [alias, canonical] of TERM_ALIASES.entries()) {
    if (canonical === term) variants.add(alias);
  }

  if (term === "스페이스X IPO") {
    variants.add("스페이스 X IPO");
    variants.add("스페이스X 상장");
    variants.add("스페이스 X 상장");
    variants.add("SpaceX IPO");
  }

  return [...variants];
}

function countCompactOccurrences(text, search) {
  if (!search) return 0;

  let count = 0;
  let index = 0;
  while (index < text.length) {
    const found = text.indexOf(search, index);
    if (found === -1) break;
    count += 1;
    index = found + search.length;
  }

  return count;
}

function escapeRegExp(value) {
  return String(value).replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

function compactForMatching(value) {
  return String(value ?? "")
    .replace(/<[^>]+>/g, "")
    .replace(/[^가-힣A-Za-z0-9]/g, "")
    .toLowerCase();
}

function isOverlappingShorterTerm(term, selectedTerms) {
  const compact = compactForMatching(term);

  return selectedTerms.some((selected) => {
    const selectedCompact = compactForMatching(selected);
    return compact.length < selectedCompact.length && selectedCompact.includes(compact);
  });
}

function cleanHtml(value) {
  return decodeEntities(
    String(value ?? "")
      .replace(/<script[\s\S]*?<\/script>/gi, " ")
      .replace(/<style[\s\S]*?<\/style>/gi, " ")
      .replace(/<[^>]+>/g, " ")
      .replace(/\s+/g, " "),
  ).trim();
}

function normalizeText(value) {
  return cleanHtml(value)
    .replace(/[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}/g, " ")
    .replace(/[가-힣]{2,4}\s*기자/g, " ")
    .replace(/(?:카톡|라인)\s*jebo\d+/gi, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function decodeEntities(value) {
  return value
    .replace(/&nbsp;/g, " ")
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&#(\d+);/g, (_, code) => String.fromCodePoint(Number(code)))
    .replace(/&#x([0-9a-f]+);/gi, (_, code) => String.fromCodePoint(Number.parseInt(code, 16)));
}

