function isSubstantiveKeyword(term, category) {
  term = canonicalTerm(term);
  if (!term) return false;
  if (STOPWORDS.has(term) || CATEGORY_STOPWORDS.has(term)) return false;
  if (GENERIC_KEYWORDS.has(term)) return false;
  if (EASY_LEARNING_TERMS.has(term) && !isLikelyAcronymTerm(term)) return false;
  if (DETAILED_GLOSSARY.has(term) || GLOSSARY.has(term)) {
    return isComparableTerm(term, category);
  }
  return isComparableTerm(term, category);
}

