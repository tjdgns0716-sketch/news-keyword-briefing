async function fetchArticleBody(article) {
  const fallback = `${article.title} ${article.description ?? ""}`;
  const urls = [article.link, article.originallink].filter(Boolean);
  const sortedUrls = urls.sort((a) => (a.includes("n.news.naver.com") ? -1 : 1));

  for (const url of sortedUrls) {
    try {
      const html = await fetchText(url);
      const extracted = extractArticleText(html);
      if (extracted.length > 250) return extracted;
    } catch {
      // A single article failure should not stop the whole scheduled run.
    }
  }

  return fallback;
}

async function fetchText(url) {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), 8000);

  try {
    const response = await fetch(url, {
      signal: controller.signal,
      headers: {
        "User-Agent":
          "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 Chrome/125 Safari/537.36",
        "Accept-Language": "ko-KR,ko;q=0.9,en-US;q=0.8,en;q=0.7",
      },
    });

    if (!response.ok) throw new Error(`본문 요청 실패: ${response.status}`);
    return await response.text();
  } finally {
    clearTimeout(timeout);
  }
}

function extractArticleText(html) {
  const candidates = [];
  const patterns = [
    /<article[^>]*id=["']dic_area["'][^>]*>([\s\S]*?)<\/article>/i,
    /<article[^>]*class=["'][^"']*_article_content[^"']*["'][^>]*>([\s\S]*?)<\/article>/i,
    /<div[^>]*id=["']dic_area["'][^>]*>([\s\S]*?)<\/div>/i,
    /<div[^>]*id=["']newsct_article["'][^>]*>([\s\S]*?)<\/div>/i,
    /<div[^>]*id=["']articleBodyContents["'][^>]*>([\s\S]*?)<\/div>/i,
    /<div[^>]*class=["'][^"']*article_body[^"']*["'][^>]*>([\s\S]*?)<\/div>/i,
    /<div[^>]*itemprop=["']articleBody["'][^>]*>([\s\S]*?)<\/div>/i,
    /<article[^>]*>([\s\S]*?)<\/article>/i,
  ];

  for (const pattern of patterns) {
    const match = html.match(pattern);
    if (match?.[1]) candidates.push(cleanArticleCandidate(match[1]));
  }

  return candidates
    .filter((candidate) => candidate.length > 80 && candidate.length < 12000)
    .sort((a, b) => b.length - a.length)[0] ?? "";
}

function cleanArticleCandidate(value) {
  return cleanHtml(value)
    .replace(/기자\s*[가-힣A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}/g, " ")
    .replace(/[가-힣]{2,4}\s*기자/g, " ")
    .replace(/무단전재\s*및\s*재배포\s*금지.*/g, " ")
    .replace(/재판매\s*및\s*DB\s*금지.*/gi, " ")
    .replace(/(?:카톡|라인)\s*jebo\d+/gi, " ")
    .replace(/Copyright.*$/gi, " ")
    .replace(/관련기사.*/g, " ")
    .replace(/다음에서\s*[^.。]*뉴스\s*구독[^.。]*/gi, " ")
    .replace(/네이버에서\s*[^.。]*뉴스\s*구독[^.。]*/gi, " ")
    .replace(/제보는\s*카카오톡[^.。]*/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function buildBriefing(articles, previous, now) {
  const previousMap = buildPreviousMap(previous);
  const categories = CATEGORIES.map((category) => {
    const categoryArticles = articles.filter((article) => article.categoryId === category.id);
    const analyzed = analyzeCategory(category, categoryArticles);

    return {
      id: category.id,
      label: category.label,
      accent: category.accent,
      words: analyzed.words.map((entry, index) =>
        makeBriefingItem(entry, index, "words", category, analyzed, previousMap),
      ),
      people: analyzed.people.map((entry, index) =>
        makeBriefingItem(entry, index, "people", category, analyzed, previousMap),
      ),
    };
  });

  dedupeRepeatedCategoryWords(categories);

  return {
    generatedAt: toKstIso(now),
    nextUpdateAt: toKstIso(getNextUpdate(now)),
    rangeText: buildRangeText(now),
    statusLabel:
      COLLECTION_MODE === "sample"
        ? "샘플 파이프라인"
        : COLLECTION_MODE === "search"
          ? "검색 API 분석"
          : "최근 24시간",
    categories: categories.map((category) => ({
      ...category,
      words: category.words.slice(0, 5),
      people: category.people.slice(0, 3),
    })),
  };
}

function dedupeRepeatedCategoryWords(categories) {
  const bestByTerm = new Map();

  for (const category of categories) {
    for (const [index, item] of category.words.entries()) {
      const current = bestByTerm.get(item.term);
      const score = (Number(item.count) || 0) * 10 + (item.articles?.length ?? 0) * 3 - index;
      if (!current || score > current.score) {
        bestByTerm.set(item.term, { categoryId: category.id, score });
      }
    }
  }

  for (const category of categories) {
    category.words = category.words.filter((item) => bestByTerm.get(item.term)?.categoryId === category.id);
  }
}

function analyzeCategory(category, articles) {
  const personCounts = new Map();
  const personDocs = new Map();
  const currentArticles = articles.filter((article) => article.comparisonPeriod !== "baseline");
  const baselineArticles = articles.filter((article) => article.comparisonPeriod === "baseline");
  const current = collectComparableTermStats(category, currentArticles);
  const baseline = collectComparableTermStats(category, baselineArticles, { collectDocs: false });

  for (const article of currentArticles) {
    const text = `${article.title} ${article.description ?? ""} ${article.body ?? ""}`;
    const titleText = `${article.title} ${article.description ?? ""}`;
    const people = new Set(Array.isArray(article.analyzedPeople) ? article.analyzedPeople : extractPeople(text));
    const relevantPeople = new Set(
      [...people].filter((person) => isRelevantPersonForCategory(person, category, text)),
    );

    for (const person of relevantPeople) {
      increment(personCounts, person);
      if (!personDocs.has(person)) personDocs.set(person, []);
      personDocs.get(person).push(article);
    }
  }

  return {
    docs: current.docs,
    termDocs: current.termDocs,
    personDocs,
    words: topComparedKeywordEntries(current.stats, baseline.stats, 8, category),
    people: topEntries(personCounts, 3),
  };
}

function makeBriefingItem(entry, index, type, category, analyzed, previousMap) {
  const { term, count } = entry;
  const docs = type === "people" ? (analyzed.personDocs.get(term) ?? []) : (analyzed.termDocs.get(term) ?? []);
  const related = findRelatedTerms(term, docs, category).slice(0, 4);
  const articles = unique(docs.map((doc) => cleanContextTitle(doc.title)).filter(isUsableArticleTitle)).slice(0, 3);
  const previousCount = previousMap.get(`${category.id}:${type}:${term}`);

  return {
    term,
    count,
    baselineCount: entry.baselineCount ?? 0,
    delta: formatDelta(count, previousCount),
    definition: buildDefinition(term, type, category, docs, related),
    context: buildContext(term, type, category, related, docs, index, entry.baselineCount ?? 0),
    related,
    articles,
  };
}

function collectComparableTermStats(category, articles, options = {}) {
  const stats = new Map();
  const termDocs = new Map();
  const docs = [];
  const collectDocs = options.collectDocs !== false;

  for (const article of articles) {
    const text = `${article.title} ${article.description ?? ""} ${article.body ?? ""}`;
    const titleText = `${article.title} ${article.description ?? ""}`;
    const people = new Set(Array.isArray(article.analyzedPeople) ? article.analyzedPeople : extractPeople(text));
    const termCounts = extractComparableTermCounts(text, category, people);
    const titleCounts = extractComparableTermCounts(titleText, category, people);

    article.analyzedTermCounts = Object.fromEntries(termCounts);
    article.analyzedTerms = [...termCounts.keys()];
    article.titleTerms = [...titleCounts.keys()];

    if (collectDocs) docs.push({ article, tokens: [], people });

    for (const [term, count] of termCounts.entries()) {
      if (count <= 0) continue;

      const entry = stats.get(term) ?? {
        count: 0,
        articleCount: 0,
        titleCount: 0,
        score: 0,
      };
      entry.count += count;
      entry.articleCount += 1;
      entry.titleCount += titleCounts.has(term) ? 1 : 0;
      entry.score += count + (titleCounts.has(term) ? 1.5 : 0);
      stats.set(term, entry);

      if (collectDocs) {
        if (!termDocs.has(term)) termDocs.set(term, []);
        termDocs.get(term).push(article);
      }
    }
  }

  return { stats, termDocs, docs, articleCount: articles.length };
}

function extractComparableTermCounts(text, category, people = new Set()) {
  const normalized = normalizeText(text);
  const counts = new Map();

  for (const candidate of extractComparableCandidates(normalized)) {
    const term = canonicalTerm(candidate);
    if (!isComparableTerm(term, category, people)) continue;
    counts.set(term, (counts.get(term) ?? 0) + 1);
  }

  for (const term of extractDynamicLearningTerms(normalized)) {
    if (!isComparableTerm(term, category, people)) continue;
    const occurrenceCount = Math.max(1, countTermOccurrences(normalized, term));
    counts.set(term, Math.max(counts.get(term) ?? 0, occurrenceCount));
  }

  return counts;
}

function extractComparableCandidates(text) {
  const candidates = [];
  const sentences = String(text)
    .split(/[.!?。！？\n\r]+/)
    .map((sentence) => sentence.trim())
    .filter(Boolean);

  for (const sentence of sentences) {
    const tokens = extractTokens(sentence).filter((token) => isComparableToken(token));

    for (const token of tokens) {
      candidates.push(token);
    }

    for (let size = 2; size <= 4; size += 1) {
      for (let index = 0; index <= tokens.length - size; index += 1) {
        const phraseTokens = tokens.slice(index, index + size);
        const phrase = phraseTokens.join(" ");
        const compactLength = compactForMatching(phrase).length;
        if (compactLength < 4 || compactLength > 28) continue;
        if (phraseTokens.every((token) => isWeakPhraseToken(token))) continue;
        candidates.push(phrase);
      }
    }
  }

  return candidates;
}

function isComparableToken(token) {
  if (!token) return false;
  if (STOPWORDS.has(token) || GENERIC_KEYWORDS.has(token) || CATEGORY_STOPWORDS.has(token)) return false;
  if (/^\d+$/.test(token)) return false;
  if (/^[a-z]+$/.test(token)) return false;
  if (/^[가-힣]{1}$/.test(token)) return false;
  if (/^[가-힣]{2}$/.test(token) && GENERIC_KEYWORDS.has(token)) return false;
  return true;
}

function isWeakPhraseToken(token) {
  return STOPWORDS.has(token) || GENERIC_KEYWORDS.has(token) || CATEGORY_STOPWORDS.has(token) || token.length <= 1;
}

function isLikelyPersonNameTerm(term, people = new Set()) {
  if (!term) return false;
  if (people.has(term) || KNOWN_PEOPLE.includes(term)) return true;
  if ([...people].some((person) => term.includes(person))) return true;
  if (KNOWN_PEOPLE.some((person) => term.includes(person))) return true;
  return /^(?:배우|가수|감독|작가|선수|코치|대표|회장|원장|의원|장관|시장)\s*[가-힣]{2,4}$/u.test(term);
}

function isComparableTerm(term, category, people = new Set()) {
  if (!term) return false;
  if (isLikelyPersonNameTerm(term, people)) return false;
  if (ORGANIZATION_OR_BRAND_TERMS.has(term)) return false;
  if ([...ORGANIZATION_OR_BRAND_TERMS].some((name) => term.includes(name))) return false;
  if (/^[A-Za-z0-9]{5,}$/u.test(term) && /[A-Za-z]/u.test(term) && /\d/u.test(term)) return false;
  if (STANDALONE_RATING_TERMS.has(term.toUpperCase())) return false;
  if (containsStandaloneRatingSymbol(term)) return false;
  if (STANDALONE_ACRONYM_NOISE_TERMS.has(term.toUpperCase())) return false;
  if (containsStandaloneAcronymNoise(term)) return false;
  if (CATEGORY_SPECIFIC_NOISE_TERMS[category.id]?.has(term)) return false;
  if (isWeatherOrForecastNoise(term)) return false;
  if (isCommodityPriceNoise(term)) return false;
  if (isStandaloneGenericProcessTerm(term)) return false;
  if (isCulturalProperNameNoise(term)) return false;
  if (isRelationshipLabelNoise(term)) return false;
  if (isOrganizationPhraseNoise(term)) return false;
  if (isLocationFragmentNoise(term)) return false;
  if (isCategoryDomainMismatch(term, category)) return false;
  if (/스페이스\s*X/u.test(term) && term !== "스페이스X IPO" && !/(IPO|상장|공모)/u.test(term)) return false;
  if (!isTermCompatibleWithCategory(term, category)) return false;
  if (term !== "재선거" && /재선거/u.test(term)) return false;
  if (term !== "선관위" && /선관위/u.test(term)) return false;
  if (term !== "신용등급" && /신용등급/u.test(term)) return false;
  if (term !== "공모가" && /공모가/u.test(term)) return false;
  if (term !== "시가총액" && /시가총액/u.test(term)) return false;
  if (term !== "증거보전" && /증거보전/u.test(term)) return false;
  if (term !== "압수수색" && /압수수색/u.test(term)) return false;
  if (term !== "집행유예" && /집행유예/u.test(term)) return false;
  if (term !== "가처분" && /가처분/u.test(term)) return false;
  if (/MOU/iu.test(term) && term !== "MOU") return false;
  if (/\bMOU$/iu.test(term) && term !== "MOU") return false;
  if (isSpecificOrganizationName(term)) return false;
  if (
    STOPWORDS.has(term) ||
    GENERIC_KEYWORDS.has(term) ||
    CATEGORY_STOPWORDS.has(term) ||
    COMPARISON_NOISE_TERMS.has(term) ||
    PRESS_NAMES.has(term)
  ) {
    return false;
  }
  if (EASY_LEARNING_TERMS.has(term) && !isLikelyAcronymTerm(term)) return false;
  if (!hasReliableTermExplanation(term)) return false;
  if (term.length < 2 || term.length > 32) return false;
  if (/^\d+$/.test(term)) return false;
  if (/^[a-z]+$/.test(term)) return false;
  if (/인공지능\s*AI|^인공지능$|^AI$/iu.test(term)) return false;
  if (/(뉴스|구독|제보|카카오톡|유튜브|채널|무단전재|재배포)/u.test(term)) return false;
  if (/^(.*\s)?(대통령|대표|장관|총리|의원|시장|지사|위원장|회장)$/u.test(term)) return false;
  if (
    term
      .split(/\s+/)
      .some(
        (part) =>
          COMPARISON_NOISE_TERMS.has(part) ||
          PRESS_NAMES.has(part) ||
          STANDALONE_ACRONYM_NOISE_TERMS.has(part.toUpperCase()),
      )
  ) {
    return false;
  }
  if (!hasOpenEndedLearningTermShape(term)) return false;
  if (/^(있다|없다|했다|됐다|한다|된다|위해|대해|통해)$/u.test(term)) return false;
  return true;
}

function isCategoryDomainMismatch(term, category) {
  if (category.id === "culture") {
    return /(종전|휴전|정전|전쟁|전투기|미군|해협|이란|트럼프|중부사령부|CENTCOM|사령부|정상회담|정상회의|공동성명|양해각서|MOU|협정|서명식|안보|군사)/iu.test(
      term,
    );
  }

  return false;
}

function hasOpenEndedLearningTermShape(term) {
  term = String(term ?? "").trim();
  if (!term) return false;
  if (hasLearnableTermShape(term)) return true;
  if (isLikelyAcronymTerm(term)) return true;
  if (containsLikelyAcronym(term) && compactForMatching(term).length <= 30) return true;

  const parts = term.split(/\s+/).filter(Boolean);
  const compactLength = compactForMatching(term).length;
  if (compactLength < 4 || compactLength > 32) return false;

  if (parts.length >= 2 && parts.length <= 4) {
    if (parts.every((part) => isWeakPhraseToken(part))) return false;
    if (parts.some((part) => isLikelyAcronymTerm(part))) return true;
    if (parts.some((part) => hasNewsConceptSuffix(part))) return true;
    if (hasNewsConceptSuffix(term)) return true;

    const substantialParts = parts.filter((part) => part.length >= 2 && !isWeakPhraseToken(part));
    if (substantialParts.length >= 2 && parts.some((part) => /[A-Za-z]/u.test(part))) return true;
  }

  if (/\p{Script=Hangul}/u.test(term) && hasNewsConceptSuffix(term)) return true;
  return false;
}

