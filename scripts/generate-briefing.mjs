function buildRangeText(now) {
  const currentEnd = getKstParts(now);
  const currentStart = getKstParts(new Date(now.getTime() - CURRENT_WINDOW_HOURS * 60 * 60 * 1000));
  return `${currentStart.year}.${pad(currentStart.month)}.${pad(currentStart.day)} ${pad(currentStart.hour)}:${pad(currentStart.minute)} - ${currentEnd.year}.${pad(currentEnd.month)}.${pad(currentEnd.day)} ${pad(currentEnd.hour)}:${pad(currentEnd.minute)}`;
}

function getNextUpdate(now) {
  const kst = getKstParts(now);
  const morning = fromKstParts(kst.year, kst.month, kst.day, 7, 30);
  const evening = fromKstParts(kst.year, kst.month, kst.day, 18, 30);

  if (now < morning) return morning;
  if (now < evening) return evening;

  const tomorrow = new Date(fromKstParts(kst.year, kst.month, kst.day, 0, 0).getTime() + 24 * 60 * 60 * 1000);
  const t = getKstParts(tomorrow);
  return fromKstParts(t.year, t.month, t.day, 7, 30);
}

function getKstParts(date) {
  const kst = new Date(date.getTime() + 9 * 60 * 60 * 1000);
  return {
    year: kst.getUTCFullYear(),
    month: kst.getUTCMonth() + 1,
    day: kst.getUTCDate(),
    hour: kst.getUTCHours(),
    minute: kst.getUTCMinutes(),
  };
}

function fromKstParts(year, month, day, hour, minute) {
  return new Date(Date.UTC(year, month - 1, day, hour - 9, minute, 0));
}

function toKstIso(date) {
  const parts = getKstParts(date);
  return `${parts.year}-${pad(parts.month)}-${pad(parts.day)}T${pad(parts.hour)}:${pad(parts.minute)}:00+09:00`;
}

function pad(value) {
  return String(value).padStart(2, "0");
}

