function keywordSelectionScore(term, category) {
  let score = 0;

  if (isLikelyAcronymTerm(term)) score += 18;
  else if (containsLikelyAcronym(term)) score += 12;

  if (hasNewsConceptSuffix(term)) score += 5;
  if (term.includes(" ")) score += 3;
  if (DETAILED_GLOSSARY.has(term) || GLOSSARY.has(term) || TERM_PROSE_OVERRIDES.has(term)) score += 2;
  if (category.id === "economy" && containsLikelyAcronym(term)) score += 3;
  if (isLikelyCompanyOrBrandKeyword(term)) score -= 30;

  return score;
}

function isLikelyCompanyOrBrandKeyword(term) {
  const canonical = canonicalTerm(term);
  if (!canonical) return false;
  if (ORGANIZATION_OR_BRAND_TERMS.has(canonical)) return true;
  if ([...ORGANIZATION_OR_BRAND_TERMS].some((name) => canonical.includes(name))) return true;
  if (isSpecificOrganizationName(canonical)) return true;
  if (/(?:Inc|Corp|Corporation|Company|Co\.|Ltd|LLC|Holdings)$/iu.test(canonical)) return true;
  if (/^[A-Z][A-Za-z0-9&.\-]+(?:\s+[A-Z][A-Za-z0-9&.\-]+){0,2}$/u.test(canonical) && !isLikelyAcronymTerm(canonical)) {
    return true;
  }
  return false;
}

function scoreKeyword(term, category, titleHit) {
  if (!isSubstantiveKeyword(term, category)) return 0;

  let score = 1;
  if (term.includes(" ")) score += 2;
  if (compactForMatching(term).length >= 4) score += 1;
  if (titleHit) score += 2;
  if (isLikelyAcronymTerm(term)) score += 3;
  if (containsLikelyAcronym(term)) score += 1;
  if (isLikelyCompanyOrBrandKeyword(term)) score -= 4;

  return Math.max(0, score);
}

function extractPeople(text) {
  const normalized = normalizeText(text);
  const people = new Set();
  const titlePattern = new RegExp(`([가-힣]{2,8}(?:\\s+[가-힣]{2,8})?)\\s*(${PERSON_TITLES.join("|")})`, "g");

  for (const match of normalized.matchAll(titlePattern)) {
    const names = normalizePersonCandidates(match[1]);
    for (const candidate of names) {
      people.add(candidate);
    }
  }

  for (const name of KNOWN_PEOPLE) {
    if (normalized.includes(name)) people.add(name);
  }

  return people;
}

function normalizePersonCandidates(value) {
  const compact = String(value ?? "")
    .trim()
    .replace(/[^가-힣\s]/g, " ")
    .replace(/\s+/g, " ")
    .trim();
  if (!compact) return [];

  const parts = compact.split(" ").filter((part) => part.length >= 2 && part.length <= 8);
  const candidates = new Set();

  for (const part of parts) {
    const normalized = normalizePersonName(part, { allowForeignStyle: true });
    if (normalized) candidates.add(normalized);
  }

  if (parts.length > 1) {
    const full = parts.join(" ");
    if (full.length <= 12) candidates.add(full);
  }

  return [...candidates];
}

