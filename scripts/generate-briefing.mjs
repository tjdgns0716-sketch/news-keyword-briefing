function normalizeToken(value) {
  let token = value.trim();
  token = token.replace(/^(기자|사진|영상|단독|속보)/, "");
  token = token.replace(/[.\-·]+$/g, "");
  token = token.replace(/(으로서는|으로써는|에서는|에게서는|까지는|부터는|이라는|라는|에서|으로|에게|보다|처럼|까지|부터|에도|에는|은|는|이|가|을|를|의|와|과|로|도|만|께|에)$/u, "");
  token = token.replace(/(했습니다|됐습니다|이었습니다|했습니다|이었다|했다|됐다|한다|된다|보도됐다|등장했다|이어졌다|다뤄졌다|올랐다)$/u, "");
  token = token.replace(/[^가-힣A-Za-z0-9+.\-·]/g, "");

  if (token.length < 2 || token.length > 20) return "";
  if (/^\d+$/.test(token)) return "";
  if (/^[A-Za-z]+\d+$/u.test(token)) return "";
  return token;
}

function normalizePersonName(value, options = {}) {
  const name = value.trim().replace(/[^가-힣]/g, "");
  if (name.length < 2 || name.length > 4) return "";
  if (!options.allowForeignStyle && !KOREAN_SURNAMES.includes(name[0])) return "";
  if (NON_PERSON_NAMES.has(name)) return "";
  if (/(위원회|위원|정부|국회|검찰|경찰|법원|은행|기업|협회|학회|연합|일보|방송|신문|공사|공단)$/u.test(name)) return "";
  if (STOPWORDS.has(name) || CATEGORY_STOPWORDS.has(name)) return "";
  return name;
}

function findRelatedTerms(term, docs, category) {
  const stats = new Map();

  for (const doc of docs) {
    const text = `${doc.title} ${doc.description ?? ""} ${doc.body ?? ""}`;
    const titleText = `${doc.title} ${doc.description ?? ""}`;
    const titleCandidates = new Set(
      Array.isArray(doc.titleTerms)
        ? doc.titleTerms
        : extractTopicCandidates(titleText, category, extractTokens(titleText)),
    );
    const candidates = Array.isArray(doc.analyzedTerms)
      ? doc.analyzedTerms
      : extractTopicCandidates(text, category);

    for (const candidate of candidates) {
      if (candidate === term) continue;

      const score = scoreKeyword(candidate, category, titleCandidates.has(candidate));
      if (score <= 0) continue;
      addKeywordStat(stats, candidate, score);
    }
  }

  return topKeywordEntries(stats, 8).map((entry) => entry.term);
}

